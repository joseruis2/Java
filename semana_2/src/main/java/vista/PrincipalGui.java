    private void procesar() {
        int codigo;
        String descripcion;
        int cantidad;
        double precioUnitarioDeventaSoles;
        double precioUnitarioDeCompraSoles;
        
        codigo = Integer.parseInt(txtCodigo.getText());
        descripcion = txtDescripcion.getText();
        cantidad = Integer.parseInt(txtCantidad.getText());
        precioUnitarioDeventaSoles = Double.parseDouble(txtPrecioUnitarioVentaEnSoles.getText());
        precioUnitarioDeCompraSoles = Double.parseDouble(txtPrecioUnitarioCompraSoles.getText());
        
        producto pro = new producto(codigo, descripcion, cantidad, precioUnitarioDeCompraSoles, precioUnitarioDeventaSoles);
        
        txtResultado.append(pro.getCodigo() + "\t" //Imprimir
                + pro.getDescripcion() + "\t"
                + pro.getCantidad() +"\t\t"
                + pro.getPrecioUnitarioCompraSoles() + "\t\t"
                + pro.getPreciounitarioventasoles() + "\t\t" 
                + pro.costodeinventariosoles()+"\t"
                + pro.utilidadUnitariaSoles() + "\t\t"
                + pro.utilidadUnitariaDolares(precioUnitarioDeCompraSoles) + "\n");
        
        
    }
